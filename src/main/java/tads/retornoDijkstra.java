package tads;

public class retornoDijkstra {
    String string;
    double entero;
    public retornoDijkstra(String string, double entero) {
        this.string = string;
        this.entero = entero;
    }

    public String getString() {
        return string;
    }

    public void setString(String string) {
        this.string = string;
    }

    public double getEntero() {
        return entero;
    }

    public void setEntero(double entero) {
        this.entero = entero;
    }
}
